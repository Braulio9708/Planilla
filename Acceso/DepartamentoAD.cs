            try
            {
                InicialisarVariablesGlovales(oDatos);

                Consultas = @"delete from departamento where IdDepartamento = @IdDepartamento;";

                Comando.Parameters.Add(new MySqlParameter("@IdDepartamento", MySqlDbType.Int32)).Value = oRegistroEN.IdDepartamento;

                Comando.ExecuteNonQuery();

                DescripcionDeLaOperacion = string.Format("El registro fue eliminado correctamente. {0} {1}", Environment.NewLine, InformacionDelRegistro(oRegistroEN));

